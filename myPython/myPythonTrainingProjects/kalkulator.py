 # калькулятор

what = input ( "Что делаем? (+, -): ")
 
a = float(input("Введи первое число:  "))
b = float (input("Введи второе число:  "))

if what == "+":
    c = a + b

    print(f'Результат сложени', a, 'и', b, 'равен', c)
elif what == "-":
    c = a - b
    print(f'Результат разницы', a, 'и', b, 'равен', c)
